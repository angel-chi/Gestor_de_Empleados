    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt, Student student) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        StudentRepository repo= new StudentRepository(LocalConnector.getLocalConnection());
        String edad;
        String matricula;
        String semestre;
        String telefono;
        String materias;
        if(jTextFieldEdad.getText().isEmpty()){
            edad="0";
        }else {
            edad=jTextFieldEdad.getText();
        }
        if(jTextFieldMatricula.getText().isEmpty()){
            matricula="0";
        }else{
            matricula=jTextFieldMatricula.getText();
        }
        if(jTextFieldSemestre.getText().isEmpty()){
            semestre="0";
        }else{
            semestre=jTextFieldSemestre.getText();
        }
        if(jTextFieldNumeroTelefono.getText().isEmpty()){
            telefono="0";
        }else{
            telefono=jTextFieldNumeroTelefono.getText();
        }
        if(jTextFieldMaterias.getText().isEmpty()){
            materias="0";
        }else{
            materias=jTextFieldMaterias.getText();
        }
        try{
            Integer.parseInt(edad);
            Long.parseLong(telefono);
            Integer.parseInt(matricula);
            Integer.parseInt(semestre);
            Long.parseLong(matricula);
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog (this, "ALGUN CAMPO NUMERICO ES INVALIDO, ASEGURESE DE QUE SEAN NUMEROS VALIDOS");

            return;
        }
        repo.update(student,jTextFieldNombre.getText(),jTextFieldApellido.getText(),jTextFieldEdad.getText(),jTextFieldGenero.getText(),jTextFieldCorreo.getText(),jTextFieldNumeroTelefono.getText(),jTextFieldMaterias.getText(),jTextFieldMatricula.getText(),jTextFieldSemestre.getText());
        dispose();

    }//GEN-LAST:event_jButtonActualizarActionPerformed

