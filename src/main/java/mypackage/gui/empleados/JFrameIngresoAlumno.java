    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        // TODO add your handling code here:

        try {
            String nombre = jTextFieldNombre.getText();
            String apellido = jTextFieldApellido.getText();
            int edad = Integer.parseInt(jTextFieldEdad.getText());
            String genero = jTextFieldGenero.getText();
            String curso = jTextFieldCurso.getText();
            String matricula = jTextFieldMatricula.getText();
            double promedio = Double.parseDouble(jTextFieldPromedio.getText());
            String email = jTextFieldEmail.getText();
            String fecha_ingreso = jTextFieldFechaIngreso.getText();

            if (nombre.isEmpty() || apellido.isEmpty() || jTextFieldEdad.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor complete todos los campos obligatorios.");
                return;
            }


            Alumno nuevoAlumno = new Alumno(
                    0,
                    nombre,
                    apellido,
                    edad,
                    genero,
                    curso,
                    matricula,
                    promedio,
                    fecha_ingreso,
                    email
            );

            alumnoRepository.save(nuevoAlumno);

            JOptionPane.showMessageDialog(this, "‚úÖ Alumno agregado correctamente.");

            jTextFieldNombre.setText("");
            jTextFieldApellido.setText("");
            jTextFieldEdad.setText("");
            jTextFieldGenero.setText("");
            jTextFieldCurso.setText("");
            jTextFieldMatricula.setText("");
            jTextFieldPromedio.setText("");
            jTextFieldEmail.setText("");
            jTextFieldFechaIngreso.setText("");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "‚ùå Ingrese valores num√©ricos donde sea necesario.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "‚ùå Error al agregar el alumno.");
            ex.printStackTrace();
        }



    }//GEN-LAST:event_jButtonAgregarActionPerformed

