    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed

        int filaTablaStudent = jTableListaStudents.getSelectedRow();

        if (filaTablaStudent == -1) {

            JOptionPane.showMessageDialog(this, "SELECCIONE ALGUN ESTUDIANTE QUE DESEE ELIMINAR DE LA LISTA!");

            return;
        }

        String idStudent = (String) jTableListaStudents.getValueAt(filaTablaStudent, 0);



        if (JOptionPane.showConfirmDialog(this, "ESTA POR ELIMINAR EL ESTUDIANTE CON EL ID " + idStudent + "!!") != 0) {

            return;

        }

        studentRepository.remove(studentRepository.getById(idStudent));

        listarStudents();
    }//GEN-LAST:event_jButtonEliminarActionPerformed

